package wed;

public class UserBean {
	String username;
	String email;
	String age;

	public void setUsername(String value) {
		username = value;
	}

	public void setEmail(String value) {
		email = value;
	}

	public void setAge(String value) {
		age = value;
	}

	public String getUsername() {
		return username;
	}

	public String getEmail() {
		return email;
	}

	public String getAge() {
		return age;
	}

}
